# f-strings are used to insert variables and expressions directly into a string.
# They are easier to read and write than using the format() function.

# Storing the string "Geeks" in a variable
val = "Geeks"

# Using an f-string to insert the value of 'val' into the string
print(f"{val}for{val} is a portal for {val}.")

# Another example of using f-strings

# Storing a name and age in variables
name = "Ganesh"
age = 19

# Inserting the variables directly into the string
# Any values stored in 'name' and 'age' will automatically appear in the output
print(f"Hello, my name is {name} and I'm {age} years old.")
