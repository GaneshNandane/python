
# This is a simple calculator that takes two predefined variables
a = 15
b = 30

# This prints the addition of two predefined variables
print("The value of", a, "+", b, "is: ", a + b)

# This prints the subtraction of two predefined variables
print("The value of", a, "-", b, "is: ", a - b)

# This prints the multiplication of two predefined variables
print("The value of", a, "*", b, "is: ", a * b)

# This prints the division of two predefined variables
print("The value of", a, "/", b, "is: ", a / b)
