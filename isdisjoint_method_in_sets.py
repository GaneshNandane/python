# The isdisjoint() method checks whether two sets have any common elements. It returns True if the sets have no common elements. It returns False if the sets have one or more common elements.

# defining two sets
cities={"Tokyo", "Madrid", "Berlin", "Delhi"}
cities2={"Tokyo", "Seoul", "Kabul", "Madrid"}

# printing if the sets are disjoint or not 
print(cities.isdisjoint(cities2))
